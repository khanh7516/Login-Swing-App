    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = txtRegisUsername.getText();
        String email = txtRegisEmail.getText();
        String password = txtRegisPassword.getText();
        String rePassword = txtRegisRePassword.getText();
        
        String error = validateRegistration(username, email, password, rePassword);
        if( error == null ) {
            userHandle.createUser(username, email, password);
            int option = JOptionPane.showConfirmDialog(rootPane, "Quay về phần đăng nhập?", "Tạo tài khoản thành công!", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            
            if (option == JOptionPane.YES_OPTION) btnBackToLoginActionPerformed(null);
            txtRegisUsername.setText("");
            txtRegisEmail.setText("");
            txtRegisPassword.setText("");
            txtRegisRePassword.setText("");
        }
        else JOptionPane.showMessageDialog(rootPane, error, "Lỗi!",JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_jButton1ActionPerformed

