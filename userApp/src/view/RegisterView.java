    public String validateRegistration(String username, String email, String password, String rePassword) {
        String usernameError = userHandle.validateUsername(username);
        String emailError = userHandle.validateEmail(email);
        String passwordError = userHandle.validatePassword(password, rePassword);

        if (usernameError == null && emailError == null && passwordError == null) {
            return null;
        } else {
            if (usernameError != null)    return usernameError; 
            else if (emailError != null)  return emailError;
            else                          return passwordError;
        }
    }
    
    
